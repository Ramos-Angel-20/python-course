# con la función len() podemos ver la longitud de un string.
print(len("Hola, como estamos?"))
print(len("@$@%@#@^"))
print(len(" "))
print(len(""))

# Concatenación
concat = "Angel" + " " + "Ramos"
print(concat)

# Podemos multiplicar un string para que este se repita varias veces.
multiplied_name = "Angel" * 30
print(multiplied_name)
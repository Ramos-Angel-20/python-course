story = 'once upon a time'

print(story.capitalize()) # convierte la primer letra del string en mayuscula
print(story.title()) # convierte todas las primeras letras de cada palabra en mayuscula
print(story.upper()) # Convierte todas las letras del string a mayusculas
print(story.lower()) # Convierte todas las letras del string a minusculas
print(story.swapcase()) # Invierte el casing de las letras, pasa mayusculas a minusculas y viceversa


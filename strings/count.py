word = "queueing"

# El metodo "count" devulve el numero de veces que
# un substring aparece dentro de un string, si
# el substring no aparece devuelve 0
print(word.count("i")) # -> devuelve 1 (i aparece 1 vez)
print(word.count("u")) # -> devuelve 2 (u aparece 2 veces)
print(word.count("e")) # -> devuelve 2 (e aparece 2 veces)
print(word.count("ue")) # -> devuelve 2 (ue aparece 2 veces)
